/// Format translation with placeholder substitution.
/// Placeholders in translations use `{name}` syntax.
/// Example: t_fmt(lang, "common.load_failed", &[("error", &msg)])
pub fn t_fmt(lang: Language, key: &'static str, args: &[(&str, &str)]) -> String {
    let template = t(lang, key);
    let mut result = template.to_string();
    for (k, v) in args {
        result = result.replace(&format!("{{{k}}}"), v);
    }
    result
}
